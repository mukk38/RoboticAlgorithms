package Mapping;

public class Nokta {
	private int x;
	private int y;
	public Nokta(int x_,int y_){
		x = x_;
		y= y_;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

}
