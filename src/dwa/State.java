package dwa;

public class State {
private	float vel;
private	float ang_vel;
private float orientation;
private	int x;
private	int y;
public float getVel() {
	return vel;
}
public void setVel(float vel) {
	this.vel = vel;
}
public float getAng_vel() {
	return ang_vel;
}
public void setAng_vel(float ang_vel) {
	this.ang_vel = ang_vel;
}
public float getOrientation() {
	return orientation;
}
public void setOrientation(float orientation) {
	this.orientation = orientation;
}
public int getX() {
	return x;
}
public void setX(int x) {
	this.x = x;
}
public int getY() {
	return y;
}
public void setY(int y) {
	this.y = y;
}

}
